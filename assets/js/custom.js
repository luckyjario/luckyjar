function metamaskExist() {
    if (typeof window.web3 == 'undefined' || !window.web3.currentProvider.isMetaMask) {
        return false;
    }
    return true;
}
function connectedWallet() {
    if (!metamaskExist()) {
        return null;
    }

    if (web3.eth.accounts.length === 0) {
        return null
    }

    return web3.eth.accounts[0];
}
function connectMetamask() {
    // Metamask not installed
    if (typeof window.web3 == 'undefined' || !window.web3.currentProvider.isMetaMask) {
        return Promise.reject({message: "MetaMask is not enabled."});
    }

    // Metamask already connected
    if (web3.eth.accounts.length > 0) {
        web3.eth.defaultAccount = web3.eth.accounts[0];
        return Promise.resolve(web3.eth.accounts[0])
    }

    // Metamask not connected
    return ethereum.enable()
        .then(() => {
            web3.eth.defaultAccount = web3.eth.accounts[0];
            return web3.eth.accounts[0];
        });

}


$(document).ready(function(){
    var wallet = null;

    connectMetamask().then(x => {
        wallet = connectedWallet();
        if(wallet){
            var wlink = "<a target='_blank' href='https://etherscan.io/address/"+
                wallet
                +"'>"+wallet+"</a>";
            $('#wallet').html(wlink); 
            $('#wallet-info').show();   
        }else{
            $('#wallet-error').show();
        }
    }).catch(x => {
        $('#wallet-error').show();
    });

    $('#invest-btn').click(function(){
        var amount = parseFloat($('#amount').val());
        if(!amount || amount < 0.01){
            alert('Minimum amount is 0.01');
            return false;
        }
        var contract = web3.eth.contract(ABI).at(
            contractAddress
        );

        web3.eth.sendTransaction({
            from: wallet,
            to: contractAddress,
            value: web3.toWei(amount, "ether")
        }, (error, txHash) => {
            if(error){
                alert(error.message || "Error");
                return false;
            }
            return alert("Invested successfully. TX Hash: "+txHash);
            return false;
        });
        return false;
    });
});